<script>
	$('#loginnn').click(function() {
		var email = document.getElementById('emailaddress').value;
		var password = document.getElementById('password').value;
		// alert(email);
			$.ajax({
    			url:"loginn.php",
        		type:"post",
				data: {
					'emailaddress' : email,
					'password' : password
				},
    			success: function(response) {
      			if (response == 'INCORRECT_CREDENTIALS') {
					Snackbar.show({
						text: 'Email or password incorrect',
						duration: 3000,
						textColor: '#fff',
						backgroundColor: '#ed0e0e'
					});
				} else if (response == 'EMPLOYER') {
					window.location = 'employer/dashboard.php';
				} else if (response == 'FREELANCER') {
					window.location = 'freelancer/dashboard.php';
				}
    		}
  		});
	});
</script>

